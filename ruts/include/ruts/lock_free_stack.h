#ifndef LF_STACK_H_
#define LF_STACK_H_

#include "atomic16B.h"

#include<cstddef>
#include<atomic>
#include<memory>
#include<cassert>
/*
 * This class provides a lock-free unbounded stack implemented
 * using a singly linked-list.
 */

namespace ruts {
  template <typename T, typename A = std::allocator<T>>
  class lf_stack {
     struct entry {
       T value;
       entry * volatile next;
     };

     struct alignas(16) versioned_head {
       entry *ptr;
       std::size_t version;

       versioned_head() : ptr(nullptr), version(0) {}
       versioned_head(entry *p, std::size_t v) : ptr(p), version(v) {}
     };

     using entry_allocator_type = typename std::allocator_traits<A>::template rebind_alloc<entry>;

     atomic16B<versioned_head> _head;
     entry_allocator_type alloc;

  public:
     lf_stack() : _head(versioned_head()), alloc() {
     }

     ~lf_stack() {
       clear();
     }

     constexpr T *head() const {
       entry *p = _head.load().ptr;
       return p ? &p->value : nullptr;
     }

     constexpr bool empty() const { return _head.load().ptr == nullptr;}

     void clear() {
       versioned_head temp = _head;
       if (temp.ptr == nullptr)
         return;

       while (true) {
         while(!_head.compare_exchange_weak(temp, versioned_head(temp.ptr->next, temp.version + 1)));
         if (temp.ptr) {
           temp.ptr->value.~T();
           alloc.deallocate(temp.ptr, 1);
         } else {
           break;
         }
       }
     }

     template <typename ...Args>
     T *allocate(Args&&... args) {
       entry *e = alloc.allocate(1);
       T *p = &(e->value);
       new (p) T(std::forward<Args>(args)...);
       e->next = nullptr;
       return p;
     }

     void push(T *p) {
       entry *e = reinterpret_cast<entry*>(reinterpret_cast<uint8_t*>(p) - offsetof(entry, value));
       versioned_head exp = _head;
       do {
         e->next = exp.ptr;
       } while(!_head.compare_exchange_weak(exp, versioned_head(e, exp.version + 1)));
     }

     void push(T *begin, T *end) {
       entry *b = reinterpret_cast<entry*>(reinterpret_cast<uint8_t*>(begin) - offsetof(entry, value));
       entry *e = reinterpret_cast<entry*>(reinterpret_cast<uint8_t*>(end) - offsetof(entry, value));
       versioned_head exp = _head;
       do {
         e->next = exp.ptr;
       } while(!_head.compare_exchange_weak(exp, versioned_head(b, exp.version + 1)));
     }

     void pop(T *&ret) {
       versioned_head exp = _head;
       while (exp.ptr) {
         ret = &(exp.ptr->value);
         if (_head.compare_exchange_weak(exp, versioned_head(exp.ptr->next, exp.version + 1))){
           return;
         }
       }
       ret = nullptr;
     }

     void deallocate(T *e) {
       alloc.deallocate(reinterpret_cast<entry*>(reinterpret_cast<uint8_t*>(e) - offsetof(entry, value)), 1);
     }
  };
}



#endif /* LF_STACK_H_ */
